    return Scaffold(
      appBar: AppBar(
        centerTitle: true,
        backgroundColor: MyAppBar.appBar.appColor,
        title: Text('予約管理'),
        bottom: TabBar(
          controller: _tabController,
          tabs: const <Widget>[
            Tab(
              text: '予約管理',
            ),
            Tab(
              text: '顧客情報管理',
            ),
          ],
        ),
      ),
